    {
        $rules = [
            'name' => 'required|min:9',
            'sku' => 'required|min:5',
            'price' => 'required|numeric'

        ];
        $validater = validator::make($request->all(),$rules);

        if ($validater->fails()){
            return redirect()->route('create')->withInput()->withErrors($validater);
        }
    }

